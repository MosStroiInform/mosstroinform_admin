package com.vasmarfas.mosstroiinformadmin.features.auth.presentation

import androidx.lifecycle.ViewModel
import androidx.lifecycle.viewModelScope
import com.vasmarfas.mosstroiinformadmin.core.network.ApiResult
import com.vasmarfas.mosstroiinformadmin.features.auth.data.AuthRepository
import kotlinx.coroutines.flow.MutableStateFlow
import kotlinx.coroutines.flow.StateFlow
import kotlinx.coroutines.flow.asStateFlow
import kotlinx.coroutines.launch

data class LoginUiState(
    val isLoading: Boolean = false,
    val error: String? = null,
    val isSuccess: Boolean = false
)

class LoginViewModel(
    private val authRepository: AuthRepository
) : ViewModel() {
    
    private val _uiState = MutableStateFlow(LoginUiState())
    val uiState: StateFlow<LoginUiState> = _uiState.asStateFlow()
    
    fun login(email: String, password: String) {
        if (email.isBlank() || password.isBlank()) {
            _uiState.value = LoginUiState(error = "Заполните все поля")
            return
        }
        
        viewModelScope.launch {
            _uiState.value = LoginUiState(isLoading = true)
            
            when (val result = authRepository.login(email, password)) {
                is ApiResult.Success -> {
                    _uiState.value = LoginUiState(isSuccess = true)
                }
                is ApiResult.Error -> {
                    _uiState.value = LoginUiState(error = result.message)
                }
                is ApiResult.Loading -> {
                    // Не используется
                }
            }
        }
    }
    
    fun clearError() {
        _uiState.value = _uiState.value.copy(error = null)
    }
}

